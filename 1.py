import numpy as np

a = np.array([1, 2, 3, 4, 5])
b = np.array([2, 3, 1, 9, 0])

print("Element wise greater than check:")
print(a > b, end="\n\n")
print("Element wise greater than or equal to check:")
print(a >= b, end="\n\n")
print("Element wise lesser than check:")
print(a < b, end="\n\n")
print("Element wise lesser than or equal to check:")
print(a <= b, end="\n\n")

